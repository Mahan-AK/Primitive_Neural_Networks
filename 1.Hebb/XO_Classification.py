    def toggle(self, i):
        if self.gridLayoutWidget.findChild(QtWidgets.QPushButton, f"pushButton_{i}").styleSheet() == "background-color: rgb(255, 255, 255);":
            self.gridLayoutWidget.findChild(QtWidgets.QPushButton, f"pushButton_{i}").setStyleSheet("background-color: rgb(0, 0, 0);")
