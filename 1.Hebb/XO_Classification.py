    def get_model(self):
        X_indexes = [0, 4, 6, 8, 12, 16, 18, 20, 24]
        O_indexes = [1, 2, 3, 5, 9, 10, 14, 15, 19, 21, 22, 23]

        x_train = [get_variant_sample(X_indexes) for _ in range(20)] + [get_variant_sample(O_indexes) for _ in range(20)]
        y_train = [[1]] * 20 + [[-1]] * 20

        w, b = train_hebb(x_train, y_train)
        f_act = lambda x: 1 if x>=0 else -1

        print("Model trained and ready!")

        return (w, b, f_act)

    def action(self, model):
        test = []
        for i in range(1, 26):
            if self.gridLayoutWidget.findChild(QtWidgets.QPushButton, f"pushButton_{i}").styleSheet() == "background-color: rgb(0, 0, 0);":
                test.append(1)
            else:
                test.append(-1)
        
        w, b, f_act = model
        pred = "X" if predict(test, w, b, f_act)[0] == 1 else "O"

        self.lbl_out.setText(f"I think it's a {pred}!")

    def reset_btns(self):
        for i in range(1, 26):
            self.gridLayoutWidget.findChild(QtWidgets.QPushButton, f"pushButton_{i}").setStyleSheet("background-color: rgb(255, 255, 255);")

        self.lbl_out.setText("")

